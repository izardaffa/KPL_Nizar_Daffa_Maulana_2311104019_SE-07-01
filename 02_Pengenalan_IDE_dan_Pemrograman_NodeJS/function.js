// named function
function perkalian(a, b) {
    let result = a * b;
    return result;
}

// expresion function 
// arrow function
// anonymous function
const penjumlahan = (a, b) => {
    let result = a + b;
    return result;
}

// named expresion function
const pembagian = function bagi(a, b) {
    let result = a / b;
    return result;
}

console.log(perkalian(5, 2));
console.log(penjumlahan(6, 2));
console.log(pembagian(8, 2));
